def tavirozsa(meret):
    dot(meret, (13, 206, 0))
    up()
    rt(90)
    fd(meret/6)
    dot(2*meret/3, (239, 75, 200))
    down()
    pensize(meret/20)
    pencolor(253, 144, 198)
    for i in range(12):
        fd(meret/3)
        back(meret/3)
        rt(15)
        fd(meret/4)
        back(meret/4)
        rt(15)
    dot(meret/3, (237, 236, 196))
    up()
    back(meret/6)
    lt(90)
    down()
def tavirozsak(meret):
    lt(10)
    tavirozsa(meret)
    for i in range(6):
        up()
