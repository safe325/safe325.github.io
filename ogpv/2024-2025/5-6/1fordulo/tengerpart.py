from turtle import *
speed(0)
colormode(255)
def tegla(a, b):
    for i in range(2):
        fd(a)
        lt(90)
        fd(b)
        lt(90)
def szinestegla(x, y, r, g, b):
    color(r, g, b)
    begin_fill()
    tegla(x, y)
    end_fill()
def hatter():
    up()
    goto(-300, -200)
    szinestegla(600, 250, 83, 205, 255)
    goto(-300, 50)
    szinestegla(600, 150, 255, 217, 90)
    goto(-300, -200)
    down()
    color(0, 0, 0)
    tegla(600, 400)
    up()
    seth(0)
def ronk(meret, tipus):
    down()
    pensize(meret/3)
    if tipus == 1:
        color(234, 136, 0)
    elif tipus == 2:
            color(144, 83, 0)
    fd(meret)
    back(meret)
    if tipus == 1:
        color(144, 83, 0)
    elif tipus == 2:
            color(234, 136, 0)
    dot(meret/4)
    up()
    color(0, 0, 0)
def leptet(d, szog):
    up()
    rt(szog)
    fd(d)
    lt(szog)
    down()
def hajo(meret):
    down()
    lt(70)
    for i in range(3):
        ronk(meret, 1)
        leptet(meret/3, 70)
        ronk(meret, 2)
        leptet(meret/3, 70)
    up()
    rt(70)
    back(meret)
    lt(70)
    fd(meret/2)
    lt(20)
    down()
    pensize(meret/8)
    pencolor(200, 200, 200)
    fd(2*meret)
    rt(90)
    pencolor("blue")
    for i in range(3):
        fd(meret/2)
        back(meret/2)
        leptet(meret/8, 90)
    up()
    seth(0)
