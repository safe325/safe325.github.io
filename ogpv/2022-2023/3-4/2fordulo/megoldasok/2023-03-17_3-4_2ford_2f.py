from turtle import *
from time import sleep
mode("logo")
def tégla(h, szín):
    speed(0)
    ht()
    fillcolor(szín)
    begin_fill()
    fd(h)
    rt(90)
    fd(h/2)
    rt(90)
    fd(h/2)
    lt(90)
    fd(h/2)
    rt(90)
    fd(h/2)
    rt(90)
    fd(h)
    rt(90)
    end_fill()
def téglalap(h):
    tégla(h, "red")
    fd(2*h)
    rt(90)
    tégla(h, "lime")
    fd(h + h/2)
    rt(90)
    fd(h)
    rt(90)
    tégla(h, "yellow")
    lt(90)
    tégla(h, "purple")
tégla(100, "red")
sleep(1)
reset()
téglalap(50)
