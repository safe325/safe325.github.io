from turtle import *
#ht()
def téglalap(a, b):
    speed(0)
    for i in range(2):
        fd(a)
        lt(90)
        fd(b)
        lt(90)
def háromszög(a):
    speed(0)
    fd(a)
    lt(135)
    fd(2**0.5*a)
    lt(135)
    fd(a)
    lt(90)
def trapéz(a, m):
    speed(0)
    fd(a)
    lt(135)
    fd(2**0.5*m)
    lt(45)
    fd(a-2*m)
    lt(45)
    fd(2**0.5*m)
    lt(135)
def pergola(m, db):
    téglalap(m/20, m)
    up()
    fd(19*m/20)
    down()
    téglalap(m/20, m)
    fd(m/20)
    lt(90)
    fd(m)
    téglalap(m/20, m)
    fd(m/20)
    lt(180)
    háromszög(m/20)
    fd(m/20)
    rt(90)
    fd(6*m/20)
    lt(135)
    trapéz(2**0.5*5*m/20, m/20/1.5)
    rt(135)
    fd(m-6*m/20-m/20)
    lt(90)
    fd(5*m/20)
    lt(135)
    trapéz(2**0.5*5*m/20, m/20/1.5)
    lt(45)
    fd(5*m/20)
    lt(90)
    fd(m/20)
    rt(90)
    fd(m/20)
    lt(90)
    háromszög(m/20)
    back(m/2-1.5*m/20/4/2)
    rt(180)
    téglalap(1.5*m/20/4, 1.5*m/20)
    fd(1.5*m/20/4/2)
    for i in range(db):
        fd(m/2/db)
        #back(1.5*m/20/4/2)
        téglalap(1.5*m/20/4, 1.5*m/20)
    back(m/2-1.5*m/20/4/2)
    for i in range(db):
        back(m/2/db)
        #back(1.5*m/20/4/2)
        téglalap(1.5*m/20/4, 1.5*m/20)
pergola(280, 2)
